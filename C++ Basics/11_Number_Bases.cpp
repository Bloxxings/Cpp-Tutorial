#include <iostream>
using namespace std;

int main() {
    int number = 0b11111111; // Binary representation (base 2)
    cout << number; // Outputs 255
    int number2 = 0xFF; // Hexadecimal representation (base 16)
    cout << number2; // Outputs 255
    int number3 = 0777; // Octal representation (base 8)
    cout << number3; // Outputs 511


    return 0;

}